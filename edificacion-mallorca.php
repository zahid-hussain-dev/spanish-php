									</p>
									Los planos de construcción normalmente son comprobados geométricamente por el topógrafo antes de su replanteo, el retranqueo legal del solar, planos de drenaje, estructuras, acabados o detalles. Debido a que los pilares y ejes de las edificaciones requieren de una alta precisión, ésta jamás se conseguirá sin un buen posicionamiento de bases, por lo que se debe prestar especial atención a la red de bases de replanteo. Riostras, zapatas, soleras… Cada elemento tiene su importancia y requiere de un cuidado específico siguiendo el pliego de condiciones para cada elemento. Las certificaciones de obra, cubicaciones y movimientos de tierra también son importantes, porque no solo de geometría vive el hombre, hay que mimar también el aspecto económico. Estos son sólo algunos de los aspectos que nos gusta mimar y trabajar en cada una de las edificaciones en las que nos hemos estado dedicando en Mallorca.
