									La construcción de una edificación en Mallorca o Baleares se divide en varias fases:
									</br>
									En primer lugar, es necesario un buen levantamiento topográfico donde aparezcan todos y cada uno de los servicios y se identifiquen, un buen levantamiento altimétrico que permita la correcta elaboración del proyecto constructivo de la edificación por parte del arquitecto y los consiguientes planos de construcción, los aislamientos, las impermeabilizaciones, los acabados, resto de detalles, una memoria y los presupuestos de las distintas partidas.
