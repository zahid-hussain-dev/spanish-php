                          <P>Un ficherpo GLM (Lenguaje de Marcado Geográfico) no es más que un archivo XLM donde aparecen las coordenadas de una parcela en sistema de coordenadas geografiacas European Terrestial Reference System 89 (ETRS89) donde además aparece alguna información adicional como la precisión del levantamiento topográfico, referencia catastral, las plantas de la edificación, el estado de la construcción entre otras cosas.</P>
                          <P>Para obtener un fichero GLM es necesario medir el terreno a través de  un levantamiento topográfico georreferenciado realizado a través de receptores GPS-GNSS (Sistema Global de Navegación por Satélite) que reciben correcciones GNSS diferenciales de bases de redes activas.</P>
